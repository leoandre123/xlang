    private TempHandle EmitCompoundOperation(BinaryExpression expression)
    {
        if (expression.Left is not VariableExpression varExpr)
            throw new NotSupportedException("Can only assign to lvalues");

        var varOp = GetVariableAsOperand(varExpr);
        var varType = GetExpressionType(expression.Left);

        var isLit = expression.Right.IsNumericLiteral();
        var litVal = isLit ? expression.Right.GetNumericalLiteralValue() : -1;

        var isPtr = false;
        var ptrTypeSize = 0;



        if (varType is PointerTypeSymbol ptr)
        {
            isPtr = true;
            ptrTypeSize = ptr.Type.Size;
            litVal *= ptrTypeSize;
        }


        if (isLit && expression.Operator.Type is (OperatorType.AddAssign or OperatorType.SubAssign or OperatorType.MulAssign))
        {
            switch (expression.Operator.Type)
            {
                case OperatorType.AddAssign: Add(varOp, new ImmediateOperand(litVal, varOp.Size)); break;
                case OperatorType.SubAssign: Sub(varOp, new ImmediateOperand(litVal, varOp.Size)); break;
                case OperatorType.MulAssign: Mul(varOp, new ImmediateOperand(litVal, varOp.Size)); break;
            }

            var tmp = _regPool.AcquireGpr();
            Move(tmp.Op, varOp);
            return tmp;
        }

        var right = EmitExpression(expression.Right);
        _regPool.EnsureRegister(right);
        if (isPtr) Mul(right.Op, new ImmediateOperand(ptrTypeSize, 4));

        switch (expression.Operator.Type)
        {
            case OperatorType.AddAssign: Add(varOp, right.Op with { Size = varOp.Size }); break;
            case OperatorType.SubAssign: Sub(varOp, right.Op with { Size = varOp.Size }); break;
            case OperatorType.MulAssign: Mul(varOp, right.Op with { Size = varOp.Size }); break;
            case OperatorType.DivAssign: Div(varOp, right.Op with { Size = varOp.Size }); break;
            case OperatorType.ModAssign: Mod(varOp, right.Op with { Size = varOp.Size }); break;
        }



        Move(right.Op, varOp);
        return right;
    }
    private TempHandle EmitAssignment(BinaryExpression expression)
    {
        var leftType = GetExpressionType(expression.Left);
        var rightType = GetExpressionType(expression.Right);


        if (LeftFirst(expression.Left, expression.Right))
        {
            var leftHandle = CalculateAddress(expression.Left);
            _regPool.EnsureRegisterAndPin(leftHandle);
            var right = EmitExpression(expression.Right);
            _regPool.EnsureRegister(right);

            Move(leftHandle.Op, right.Reg);
            FreeHandle(leftHandle);
            return right;
        }
        else
        {
            var right = EmitExpression(expression.Right);
            _regPool.EnsureRegisterAndPin(right);
            var leftHandle = CalculateAddress(expression.Left);
            Move(leftHandle.Op, right.Reg);

            FreeHandle(leftHandle);
            UnpinHandle(right);
            return right;
        }
    }

