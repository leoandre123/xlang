    public CodeGenerator(Module module, SemanticModel semanticModel, string source)
    {
        _module = module;
        _program = module.Program;
        _model = semanticModel;
        _source = source;

        _regPool = new TemporaryPool((op0, op1) => Move(op0, op1), (op0, op1) => Exchange(op0, op1));
    }

    public string Generate()
    {
        Logger.LogDebug($"Generating machine code {_program.Id}");

        List<string> externalFunction = ["ExitProcess", "GetProcessHeap", "HeapAlloc",
            ..(!_model.ExternalFunctions.ContainsKey(_module.Id) ?[]: _model.ExternalFunctions[_module.Id]
                .Select(y => Utils.Utils.SanitizeFqn(_model.GetSymbol<CallableSymbol>(y).FullyQualifiedName)))];
        var globals = _model.Symbols.Values.Where(x => x is FunctionSymbol { AccessType: EAccessType.Public } fs && x.OwnerModule.Program.Id == _program.Id).Select(y => Utils.Utils.SanitizeFqn((y as FunctionSymbol).FullyQualifiedName)).ToList();


        if (_model.GetEntryPoint()?.OwnerModule.Program.Id == _program.Id)
        {
            var label = Utils.Utils.SanitizeFqn(_model.GetEntryPoint()!.FullyQualifiedName);

            if (!globals.Contains(label))
            {
                globals.Add(label);
            }
        }

        GenerateSignature();

        _writer.Append("bits 64", false);
        _writer.Append("default rel", false);

        _writer.Append("section .rdata", false);

        for (var i = 0; i < _model.LiteralConstants.Count; i++)
        {
            _writer.Append(_model.LiteralConstants[i] switch
            {
                StringConstant s => $"LC{i}:\tdb \"{s.Text}\"".PadRight(50) + $"; {s.Text}",
                FloatConstant f => $"LC{i}:\tdd 0x{BitConverter.SingleToInt32Bits(f.Value):X8}".PadRight(50) + $"; {f.Value}f",
                _ => throw new ArgumentOutOfRangeException()
            }, false);
        }

        _writer.Append("section .text", false);
        if (externalFunction.Count != 0)
            _writer.Append($"extern {string.Join(", ", externalFunction)}");
        if (globals.Count != 0)
            _writer.Append($"global {string.Join(", ", globals)}");
        _writer.Append("");

        GenerateScope(_program.Body);

        return _writer.ToString();
    }

    public void GenerateSignature()
    {
        string[] lines = [
            "Generated by XLC version 0.0.0.1",
            $"{DateTime.Now:yyyy-MM-dd HH:mm}",
            $"Optimization: {_optimizationLevel}"
        ];

        var maxLength = lines.Max(x => x.Length);

        _writer.Comment($"##{"".PadRight(maxLength,'#')}##");
        foreach (var line in lines)
        {
            _writer.Comment($"# {line.PadRight(maxLength)} #");
        }
        _writer.Comment($"##{"".PadRight(maxLength, '#')}##");
    }

    public void GenerateScope(ScopeBody scope)
    {
        foreach (var s in scope.Scopes)
        {
            GenerateScope(s.Body);
        }
        foreach (var c in scope.Classes)
        {
            GenerateClass(c);
        }

        foreach (var f in scope.Functions)
        {
            if (f.IsExtern)
                continue;

            _currentFunction = f;
            GenerateFunction(f);
        }

    }

    public void GenerateClass(ClassDeclaration classDeclaration)
    {
        foreach (var member in classDeclaration.Members)
        {
            if (member is FunctionDeclaration methodDeclaration)
            {
                GenerateFunction(methodDeclaration);
            }
        }
    }

    public void GenerateFunction(FunctionDeclaration function)
    {
        var functionSymbol = _model.GetSymbol<FunctionSymbol>(function.Id);
        var meta = _model.FunctionData[functionSymbol.Id];
        _currentFunctionMetaData = meta;
        _isMain = _model.EntryPoint == functionSymbol.Id;
        _rspParity = 0;
        _tempCursor = 0;
        _tempMax = 0;
        _localsSize = meta.LocalsSize;
        _currentFunction = function;
        _writer.Label(Utils.Utils.SanitizeFqn(functionSymbol.FullyQualifiedName));


        for (var i = 0; i < _parameterSpilled.Length; i++)
            _parameterSpilled[i] = false;


        _regPool.Reset();

        foreach (var parSymId in functionSymbol.Parameters)
        {
            var parSym = _model.GetSymbol<ParameterSymbol>(parSymId);
            var tmp = _regPool.AcquireParameter(parSym.Index, parSym.Type.IsFloat);

            _currentParameters.Add(tmp);
        }


        //EmitFunctionPrologue(function);
        _writer.Append("___prologue___", false);
        EmitStatement(function.Body!);



        if (functionSymbol.Type == Types.Void)
        {
            if (_isMain)
            {
                Move(RegisterOperand.Rcx, ImmediateOperand.Zero);
                EmitFunctionCall("ExitProcess", 0);
            }
            else
            {
                if (!_currentFunctionMetaData.IsLeaf)
                {
                    Move(RegisterOperand.Rsp, RegisterOperand.Rbp);
                    Pop(RegisterOperand.Rbp);
                }
                _writer.Append("ret");
            }
        }
        InsertFunctionPrologue(function);
    }

    public void InsertFunctionPrologue(FunctionDeclaration function)
    {
        var functionSymbol = _model.GetSymbol<FunctionSymbol>(function.Id);
        //var meta = _model.FunctionData[functionSymbol.Id];

        _writer.SetReplaceLine("___prologue___");

        _writer.Comment($"Locals: {_currentFunctionMetaData.LocalsSize} bytes");
        _writer.Comment($"Leaf: {_currentFunctionMetaData.IsLeaf}");
        _writer.Comment($"Params: \n{string.Join('\n', functionSymbol.Parameters.Select((x, i) =>
            $"{_model.GetSymbol(x).Name}: {AssemblyUtils.GetParameterRegister(i).Name}, {AssemblyUtils.GetParameterHome(i).AsmName}"
            ))}");
        _writer.Comment("Prologue");

        if (!_currentFunctionMetaData.IsLeaf || _optimizationLevel == OptimizationLevel.None)
        {
            Push(RegisterOperand.Rbp);
            Move(RegisterOperand.Rbp, RegisterOperand.Rsp);
        }
        _rspParity = 0;


        var frameSize = _localsSize + _tempMax;
        if (frameSize != 0)
            _writer.Append($"sub rsp, {frameSize}");

        //for (var i = 0; i < Math.Min(4, function.Parameters.Count + (function.IsInstance ? 1 : 0)); i++)
        //{
        //    Move(AssemblyUtils.GetParameterLocation(i, true), AssemblyUtils.GetParameterLocation(i, false));
        //}

        _writer.Comment("Prologue End");

        _writer.RestoreLine();
    }

    public void EmitStatement(Statement statement)
    {
        if (statement is (Return or Break or VariableDeclaration or Expression))
        {
            _writer.Comment(_source.Substring(statement.Span.Start, statement.Span.Length));
        }

        Debug.WriteLine("###################################");
        Debug.WriteLine("STATEMENT: ");
        Debug.WriteLine(_regPool.FreeCount);
        Debug.WriteLine(_source.Substring(statement.Span.Start, statement.Span.Length));
        Debug.WriteLine("###################################");
        switch (statement)
        {
            case Block block:
                {
                    foreach (var s in block.Statements)
                        EmitStatement(s);
                    break;
                }
            case VariableDeclaration declaration:
                EmitVariableDeclaration(declaration);
                break;
            case IfStatement ifStatement:
                {
                    var endIfLabel = _writer.CreateLabel("endif");
                    var elseLabel = ifStatement.Else == null ? "" : _writer.CreateLabel("else");

                    _writer.Comment($"if({_source.Substring(ifStatement.Expression.Span.Start, ifStatement.Expression.Span.Length)})");

                    var tmp = EmitExpression(ifStatement.Expression);
                    _regPool.EnsureRegister(tmp);
                    Test(tmp.Reg, tmp.Reg);
                    _regPool.Release(tmp);
                    Jump(ifStatement.Else == null ? endIfLabel : elseLabel, Comparison.Zero);
                    EmitStatement(ifStatement.Body);

                    if (ifStatement.Else != null)
                    {
                        Jump(endIfLabel);
                        _writer.Label(elseLabel);
                        EmitStatement(ifStatement.Else);
                    }

                    _writer.Label(endIfLabel);
                    break;
                }
            case WhileStatement whileStatement:
                {
                    var loopLabel = _writer.CreateLabel("loop");
                    var endWhileLabel = _writer.CreateLabel("endWhile");
                    _currentBreakLabel = endWhileLabel;

                    _writer.Label(loopLabel);
                    var tmp = EmitExpression(whileStatement.Expression);
                    _regPool.EnsureRegister(tmp);
                    Test(tmp.Reg, tmp.Reg);
                    _regPool.Release(tmp);
                    Jump(endWhileLabel, Comparison.Zero);
                    EmitStatement(whileStatement.Body);
                    Jump(loopLabel);
                    _writer.Label(endWhileLabel);

                    break;
                }
            case ForStatement forStatement:
                {
                    var loopLabel = _writer.CreateLabel("loop");
                    var endForLabel = _writer.CreateLabel("endFor");
                    _currentBreakLabel = endForLabel;

                    EmitStatement(forStatement.Initial);
                    _writer.Label(loopLabel);
                    var tmp = EmitExpression(forStatement.Expression);
                    Test(tmp.Reg, tmp.Reg);
                    _regPool.Release(tmp);
                    Jump(endForLabel, Comparison.Zero);
                    EmitStatement(forStatement.Body);
                    EmitStatement(forStatement.Increment);
                    Jump(loopLabel);
                    _writer.Label(endForLabel);
                    break;
                }
            case Break:
                Jump(_currentBreakLabel);
                break;
            case Expression expression:
                EmitExpressionInto(expression, null);
                break;
            case Return returnStatement:
                EmitReturn(returnStatement);
                break;
            default:
                {
                    throw new Exception($"Unknown statement type{statement.GetType()}");
                }
        }
    }

    public void EmitVariableDeclaration(VariableDeclaration declaration)
    {
        var symbol = _model.GetSymbol<VariableSymbol>(declaration.Id);

        if (declaration.Initial != null)
        {
            if (declaration is not ArrayDeclaration)
            {
                EmitVariableAssignment(GetVariableDeclarationLocation(declaration), declaration.Initial);
            }
            else
            {
                if (declaration.Initial is ArrayInitializerListExpression aile)
                {
                    for (var i = 0; i < aile.Elements.Count; i++)
                    {
                        EmitVariableAssignment(GetVariableDeclarationLocation(declaration, i), aile.Elements[i]);
                    }
                }
            }
        }
        else
        {

            for (var i = 0; i < symbol.Count; i++)
                Move(GetVariableDeclarationLocation(declaration, i), ImmediateOperand.Zero);
        }

    }
    private void EmitReturn(Return returnStatement)
    {
        if (returnStatement.Expression != null)
        {
            EmitExpressionInto(returnStatement.Expression, RegisterOperand.Rax);
        }

        if (_isMain)
        {
            Move(RegisterOperand.Rcx, RegisterOperand.Rax);
            EmitFunctionCall("ExitProcess", 0);
        }
        else
        {
            if (_currentFunctionMetaData.IsLeaf && _optimizationLevel != OptimizationLevel.None)
            {
                var frameSize = _localsSize + _tempMax;
                if (frameSize != 0)
                    _writer.Append($"add rsp, {frameSize}");
            }
            else
            {
                Move(RegisterOperand.Rsp, RegisterOperand.Rbp);
                Pop(RegisterOperand.Rbp);
            }
            _writer.Append("ret");
        }
    }



    private TempHandle EmitExpression(Expression expression)
    {
        var exprType = GetExpressionType(expression);
        Debug.Assert(FitsInRegister(exprType.Size) || exprType.Size == 0);
        switch (expression)
        {
            case IntegerLiteralExpression intLit:
                return TempHandle.Imm(intLit.Value, 4);
            case FloatLiteralExpression floatLit:
                {
                    var size = floatLit.IsDouble ? 8 : 4;
                    var xr = _regPool.AcquireXmm();
                    Move(xr.Reg, MemoryOperand.FromLabel($"LC_{_model.LiteralConstantMap[floatLit.Id]}", size));
                    return xr;
                }
            case BooleanLiteralExpression boolLit:
                return TempHandle.Imm(boolLit.Value ? 1 : 0, 4);
            case StringLiteralExpression strLit:
                var strConstId = _model.LiteralConstantMap[strLit.Id];
                var strConst = _model.LiteralConstants[strConstId] as StringConstant;
                var strLbl = $"LC{strConstId}";
                EmitHeapAllocation(new ImmediateOperand(16, 8));
                var strPtr = _regPool.AcquireReturn(false);
                Move(MemoryOperand.FromDisplacement(strPtr.Reg.Register, 0, 8), MemoryOperand.FromLabel(strLbl, 8));
                Move(MemoryOperand.FromDisplacement(strPtr.Reg.Register, 8, 4), new ImmediateOperand(strConst.Text.Length, 4));
                return TempHandle.Gpr(RegisterOperand.Rax, 8);

            case VariableExpression varExpr:
                {
                    var sym = _model.GetSymbol<SymbolWithType>(varExpr.Id);

                    if (sym is FieldSymbol fieldSymbol)
                    {
                        var thisPtr = GetParameterHandle(0);
                        if (thisPtr.IsRegister)
                            return TempHandle.StableMem(thisPtr, fieldSymbol.Offset, exprType.Size, exprType.IsFloat);
                        var tmp = _regPool.AcquireGpr();
                        Move(tmp.Reg, thisPtr.Op);
                        return TempHandle.OwnedMem(tmp, fieldSymbol.Offset, exprType.Size, exprType.IsFloat);
                    }
                    else
                    {
                        var op = GetVariableAsOperand(varExpr);
                        return TempHandle.Any(op, exprType.Size, exprType.IsFloat);
                    }
                }
            case CallExpression callExpr:
                EmitCallExpression(callExpr);
                return exprType.IsFloat
                    ? TempHandle.Xmm(RegisterOperand.Xmm0, exprType.Size)
                    : TempHandle.Gpr(RegisterOperand.Rax, exprType.Size);
            case UnaryExpression unaryExpr:
                return EmitUnary(unaryExpr);
            case BinaryExpression binaryExpr:
                return EmitBinary(binaryExpr);
            case IndexExpression indExpr:
                {
                    var addrHandle = CalculateAddress(indExpr);
                    var res = _regPool.AcquireRegister(exprType.IsFloat);
                    Move(res.Op, addrHandle.Op);
                    FreeHandle(addrHandle);
                    return res;
                }
            case MemberAccessExpression accExpr:
                {
                    var b = EmitExpression(accExpr.Base);
                    var fieldSymbol = _model.GetSymbol<FieldSymbol>(accExpr.Id);
                    Add(b.Op, new ImmediateOperand(fieldSymbol.Offset, 8));
                    return b;
                }
            case MemberCallExpression memCallExpr:
                EmitMemberCallExpression(memCallExpr);
                return exprType.IsFloat
                    ? TempHandle.Xmm(RegisterOperand.Xmm0, exprType.Size)
                    : TempHandle.Gpr(RegisterOperand.Rax, exprType.Size);
            case MakeExpression makeExpression:
                return EmitMakeExpression(makeExpression);
            default:
                throw new NotSupportedException($"Expression not handled: {expression.GetType().Name}");
        }
    }
