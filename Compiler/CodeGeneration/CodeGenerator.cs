    /// <summary>
    /// Return a handle for a MemoryOperand pointing to the L-Value of the expression
    /// </summary>
    private TempHandle CalculateAddress(Expression expression)
    {
        var exprType = GetExpressionType(expression);
        switch (expression)
        {
            case VariableExpression varExpr:
                {
                    var sym = _model.GetSymbol<SymbolWithType>(varExpr.Id);
                    if (sym is FieldSymbol fieldSymbol)
                    {
                        var thisPtr = GetParameterLocation(_model.GetSymbol<ParameterSymbol>(_currentFunction.Parameters[0].Id));
                        if (thisPtr is RegisterOperand reg)
                        {
                            var tmp = _regPool.AcquireGpr();
                            Lea(tmp.Reg, MemoryOperand.FromDisplacement(reg.Register, fieldSymbol.Offset, exprType.Size));
                            return TempHandle.OwnedMem(tmp, 0, exprType.Size, exprType.IsFloat);
                        }
                        else
                        {
                            var tmp = _regPool.AcquireGpr();
                            Move(tmp.Reg, thisPtr);
                            Lea(tmp.Reg, MemoryOperand.FromDisplacement(tmp.Reg.Register, fieldSymbol.Offset, exprType.Size));
                            return TempHandle.OwnedMem(tmp, 0, exprType.Size, exprType.IsFloat);
                        }
                    }
                    else
                    {
                        var varOp = GetVariableAsOperand(varExpr);
                        if (varOp is RegisterOperand varReg) throw new NotSupportedException();
                        return TempHandle.StableMem(varOp as MemoryOperand, exprType.Size, exprType.IsFloat);
                    }
                }
            case IndexExpression indExpr:
                {
                    if (LeftFirst(indExpr.Base, indExpr.Base))
                    {
                        var bse = EmitExpression(indExpr.Base);
                        _regPool.EnsureRegisterAndPin(bse);
                        var idx = EmitExpression(indExpr.Index);
                        _regPool.EnsureRegister(idx);
                        Lea(idx.Reg, MemoryOperand.FromIndex(bse.Reg.Register, idx.Reg.Register, exprType.Size, 0, exprType.Size));
                        _regPool.Release(bse);
                        return idx;
                    }
                    else
                    {
                        var idx = EmitExpression(indExpr.Index);
                        _regPool.EnsureRegisterAndPin(idx);
                        var bse = EmitExpression(indExpr.Base);
                        _regPool.EnsureRegister(bse);
                        Lea(idx.Reg, MemoryOperand.FromIndex(bse.Reg.Register, idx.Reg.Register, exprType.Size, 0, exprType.Size));
                        _regPool.Release(bse);
                        return idx;
                    }
                }
            case MemberAccessExpression memberAccess:
                {
                    var fieldSym = _model.GetSymbol<FieldSymbol>(memberAccess.Id);

                    var bse = EmitExpression(memberAccess.Base);
                    _regPool.EnsureRegister(bse);

                    var tmp = _regPool.AcquireGpr();
                    Lea(tmp.Reg, MemoryOperand.FromDisplacement(bse.Reg.Register, fieldSym.Offset, exprType.Size));

                    _regPool.Release(bse);
                    return TempHandle.OwnedMem(tmp, 0, exprType.Size, exprType.IsFloat);
                }
        }

        throw new NotSupportedException();
    }


    private void EmitCallExpression(CallExpression callExpression)
    {
        var funcSym = _model.GetSymbol<CallableSymbol>(callExpression.Id);
        var callName = Utils.Utils.SanitizeFqn(funcSym.FullyQualifiedName);

        SpillParameters();
        EmitFunctionCall(callName, callExpression.Arguments);
    }
    private void EmitMemberCallExpression(MemberCallExpression memCallExpr)
    {
        var methSym = _model.GetSymbol<CallableSymbol>(memCallExpr.Id);
        var callName = Utils.Utils.SanitizeFqn(methSym.FullyQualifiedName);
        var baseType = _model.ExpressionData[memCallExpr.Base.Id].Type;

        if (baseType is ScopeTypeSymbol)
        {
            EmitFunctionCall(callName, memCallExpr.Arguments);
        }
        else
        {
            Debug.Assert(baseType is ClassTypeSymbol);
            EmitFunctionCall(callName, [memCallExpr.Base, .. memCallExpr.Arguments]);
        }
    }
    private TempHandle EmitMakeExpression(MakeExpression makeExpression)
    {
        var classSymbol = _model.GetSymbol<ClassSymbol>(makeExpression.Id);
        var constructorSymbol = _model.GetSymbol<CallableSymbol>(makeExpression.Call.Id);
        var callName = Utils.Utils.SanitizeFqn(constructorSymbol.FullyQualifiedName);
        var arguments = (makeExpression.Call as CallExpression)?.Arguments ??
                        (makeExpression.Call as MemberCallExpression)!.Arguments;



        EmitHeapAllocation(new ImmediateOperand(classSymbol.Type.Size, 8));
        var tmp = _regPool.AcquireGpr();
        Move(tmp.Reg, RegisterOperand.Rax); //TODO: Ensure constructor returns this so this line can be removed
        EmitFunctionCall(callName, RegisterOperand.Rax, arguments);

        return tmp;
    }
    private TempHandle EmitBinary(BinaryExpression binaryExpression, Operand? destination = null)
    {
        var resType = GetExpressionType(binaryExpression);
        if (binaryExpression.Operator.Type == OperatorType.Assign)
        {
            return EmitAssignment(binaryExpression);

        }

        //if (_model.ExpressionData[binaryExpression.Id].Type.Name == "string")
        //{
        //    EmitStringOperation(binaryExpression);
        //    return;
        //}

        if (Operator.CompoundOperatorTypes.Contains(binaryExpression.Operator.Type))
        {
            return EmitCompoundOperation(binaryExpression);
        }
        var (expr0, expr1) = OrderExpressions(binaryExpression.Left, binaryExpression.Right);

        var left = EmitExpression(expr0);
        //_regPool.EnsureRegisterAndPin(left);
        //_regPool.EnsureNotMemory(left);
        var right = EmitExpression(expr1);
        //_regPool.EnsureRegister(right);
        //_regPool.Unpin(left);

        if (expr0 != binaryExpression.Left)
        {
            (left, right) = (right, left);
        }

        var willModify = WillModifyOperands(binaryExpression, left, right);
        if (willModify.left) _regPool.EnsureNotMemory(left);
        if (willModify.right) _regPool.EnsureNotMemory(right);

        if (binaryExpression.Operator.Type is OperatorType.Div or OperatorType.Mod)
        {
            _regPool.EnsureSpecificRegister(left, Register.Rax);
            _regPool.EnsureSpecificRegister(right, Register.Rdx);
        }

        if (!left.IsRegister && !right.IsRegister)
        {
            //_regPool.EnsureRegister(left);
        }


        var returnInRight = false;

        switch (binaryExpression.Operator.Type)
        {
            case OperatorType.Add:
                returnInRight = Add(left.Op, right.Op);
                break;

            case OperatorType.Sub:
                returnInRight = Sub(left.Op, right.Op);
                break;

            case OperatorType.Mul:
                returnInRight = Mul(left.Op, right.Op);
                break;

            case OperatorType.Div:
                Div(left.Op, right.Op);
                break;

            case OperatorType.Mod:
                Mod(left.Op, right.Op);
                returnInRight = true;
                break;

            case OperatorType.Equal:
                Cmp(left.Op, right.Op);
                Setcc(left.Op, Comparison.Equal);
                ZeroExtend((RegisterOperand)left.Op with { Size = 1 });
                break;


            case OperatorType.NotEqual:
                Cmp(left.Op, right.Op);
                Setcc(left.Op, Comparison.NotEqual);
                ZeroExtend((RegisterOperand)left.Op with { Size = 1 });
                break;

            case OperatorType.Less:
                Cmp(left.Op, right.Op);
                Setcc(left.Op, Comparison.LessThan);
                ZeroExtend((RegisterOperand)left.Op with { Size = 1 });
                break;

            case OperatorType.LessOrEqual:
                Cmp(left.Op, right.Op);
                Setcc(left.Op, Comparison.LessOrEqual);
                ZeroExtend((RegisterOperand)left.Op with { Size = 1 });
                break;

            case OperatorType.Greater:
                Cmp(left.Op, right.Op);
                Setcc(left.Op, Comparison.GreaterThan);
                ZeroExtend((RegisterOperand)left.Op with { Size = 1 });
                break;

            case OperatorType.GreaterOrEqual:
                Cmp(left.Op, right.Op);
                Setcc(left.Op, Comparison.GreaterOrEqual);
                ZeroExtend((RegisterOperand)left.Op with { Size = 1 });
                break;
            default:
                throw new NotSupportedException($"Binary {binaryExpression.Operator.Symbol}");
        }



        _regPool.Release(returnInRight ? left : right);
        return returnInRight ? right : left;
    }
    private TempHandle EmitUnary(UnaryExpression unaryExpression)
    {
        switch (unaryExpression.Operator.Type)
        {
            case OperatorType.PreIncrement:
                return EmitIncrementDecrement(unaryExpression.Operand, true, true);
            case OperatorType.PreDecrement:
                return EmitIncrementDecrement(unaryExpression.Operand, false, true);
            case OperatorType.PostIncrement:
                return EmitIncrementDecrement(unaryExpression.Operand, true, false);
            case OperatorType.PostDecrement:
                return EmitIncrementDecrement(unaryExpression.Operand, false, false);
            case OperatorType.Neg:
                {
                    var op = EmitExpression(unaryExpression.Operand);
                    _regPool.EnsureRegister(op);
                    Neg(op.Op);
                    return op;
                }
            case OperatorType.Address:
                {
                    var varExpr = (unaryExpression.Operand as VariableExpression) ?? throw new Exception("Tried to get the address of an r-value");
                    var loc = GetVariableAsOperand(varExpr);
                    var tmp = _regPool.AcquireGpr();
                    Lea(tmp.Reg, loc);
                    return tmp;
                }
            case OperatorType.Dereference: // *a
                {
                    var resType = GetExpressionType(unaryExpression);

                    var op = EmitExpression(unaryExpression.Operand);
                    _regPool.EnsureRegister(op);
                    Move(op.Reg, MemoryOperand.FromRegister(op.Reg.Register, 8));
                    return op;
                }
            case OperatorType.LogicalNot: // !
                {
                    var op = EmitExpression(unaryExpression.Operand);
                    _regPool.EnsureRegister(op);
                    Cmp(op.Reg, ImmediateOperand.Zero);
                    Setcc(op.Reg, Comparison.Zero);
                    ZeroExtend(op.Reg with { Size = 1 });
                    return op;
                }

            default: throw new NotSupportedException($"Unary not handled '{unaryExpression.Operator.Symbol}'");
        }
    }

