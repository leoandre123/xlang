    int SethiUllman(Expression expr)
    {
        int Junction(Expression left, Expression right)
        {
            var l = SethiUllman(left);
            var r = SethiUllman(right);
            return (l == r) ? l + 1 : Math.Max(l, r);
        }

        switch (expr)
        {
            case BinaryExpression bin: return Junction(bin.Left, bin.Right);
            case UnaryExpression un: return SethiUllman(un.Operand);           //IF I CAN ENSURE IN MEMORY INC/DEC
            case IndexExpression ind: return Junction(ind.Base, ind.Index);
            case MemberAccessExpression mem: return SethiUllman(mem.Base);
            default: return 1;
        }
    }

    private bool LeftFirst(Expression left, Expression right)
    {
        if (ContainsCall(left) && !ContainsCall(right)) return true;
        if (ContainsCall(right) && !ContainsCall(left)) return false;
        return SethiUllman(left) >= SethiUllman(right);
    }
    private (Expression expr0, Expression expr1) OrderExpressions(Expression left, Expression right) => LeftFirst(left, right) ? (left, right) : (right, left);

    private IEnumerable<(Expression expression, int index)> OrderExpressions(IEnumerable<Expression> expressions)
    {
        return expressions.Select((exp, ind) => (exp, ind)).OrderByDescending(x => ContainsCall(x.exp)).ThenByDescending(y => SethiUllman(y.exp));
    }


