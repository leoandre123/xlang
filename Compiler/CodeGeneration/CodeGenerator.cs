    private void EmitFunctionCall(string name, IReadOnlyList<Expression> arguments)
    {
        //Function1(1, x, Func2(1, 5), 5);
        EmitFunctionCall(name,
            arguments.Count,
            () =>
            {
                _regPool.SpillAll();

                var numCalls = arguments.Count(ContainsCall);
                var sortedArguments = OrderExpressions(arguments);

                var callCount = 0;
                foreach (var arg in sortedArguments)
                {
                    if (ContainsCall(arg.expression)) callCount++;

                    if (callCount < numCalls)
                    {
                        // AllocateTemp(bytes);
                        EmitExpressionInto(arg.expression, AssemblyUtils.GetParameterLocation(arg.index, false));
                    }
                    else if (callCount == numCalls)
                    {
                        EmitExpressionInto(arg.expression, AssemblyUtils.GetParameterLocation(arg.index, false));
                    }
                    else
                    {
                        EmitExpressionInto(arg.expression, AssemblyUtils.GetParameterLocation(arg.index, false));
                    }
                }
            }
        );
    }
    private void EmitFunctionCall(string name, int argCount, Action? preCall = null)
    {

        var reserve = Math.Max(argCount * 8, 32);

        if (_rspParity % 16 != 0) reserve += (16 - _rspParity % 16);

        SubRsp(reserve);

        preCall?.Invoke();
        _writer.Append($"call {name}");
        AddRsp(reserve);
    }

